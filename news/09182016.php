<h2 id="09182016">September 18, 2016</h2>
<p>Feature improvement time! So, the spell/cantrip system never really jelled with me and I had been longing to replace it with something much more user friendly. So, that is what I've done. The whole spell/cantrip system has been redone. Here's a list of the changes:</p>
<p align="center"><img src="images/grimoire.PNG"></p>
<ul>
	<li>5e spells may now be added via a dropdown list near the top of the character spells page,</li>
	<li>Custom spells may be created by clicking a button just below the spell selector,</li>
	<li>DMs may create custom spells for their campaign,</li>
	<ul>	
		<li>These campaign spells will be added to the spell selector dropdown for every player character in the campaign,</li>
	</ul>	
	<li>Re-formatted the spell presentation for readability,</li>
	<li>Added spell slots,</li>
	<li>Added Ki Points for Monks,</li>
	<li>Added old spell content recovery to the character settings page:</li>
	<ul>	
		<li>Open your character sheet,</li>
		<li>Click "Settings",</li>
		<li>Click "Recover Spell List".</li>
	</ul>	
</ul>
<p>This is a pretty big change. Since it will affect most everybody using the spells page, I'd like to hear what people think. Is it better? Is it frustrating? Need help finding your old stuff? Write to me at <a href="mailto:admin@dmroyale.com">admin@dmroyale.com</a> and I'll write back!</p>
